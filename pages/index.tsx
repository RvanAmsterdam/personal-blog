import type { NextPage } from "next";
import Head from "next/head";
import styles from "../styles/Home.module.scss";
import fs from "fs";
import { parseMarkdown } from "../components/parseMarkdown";
import { ArticleProps } from "../types/ArticleProps";
import { Articles } from "../components/Articles";

const Home: NextPage = ({ articlesSortedByDate }: any) => {
  return (
    <div className={"home container"}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <section>
          <h1>header</h1>
          <Articles articles={articlesSortedByDate} />
        </section>
        <section>
          <h2>about card</h2>
        </section>
      </main>
    </div>
  );
};

export const getStaticProps = async () => {
  const files = fs.readdirSync("posts");
  const articles = files.map((file) => parseMarkdown(file).metadata);
  const articlesSortedByDate = articles.sort((a, b) => new Date(b.date).valueOf() - new Date(a.date).valueOf());

  return {
    props: {
      articlesSortedByDate,
    },
  };
};

export default Home;
